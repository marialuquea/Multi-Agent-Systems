
public class Item {
	
	// Attributes
	String _name;
	int _starting_price;
	
	// Methods
	public String getName()
	{
		return this._name;
	}
	
	public void setName(String name)
	{
		this._name = name;
	}
	
	public int getPrice()
	{
		return this._starting_price;
	}
	
	public void setPrice(int price)
	{
		this._starting_price = price;
	}
}
